package application.base.domain.enrollmentManagement;

public enum EnrollmentsStatus {
    /**
     * Indicates that the student's enrollment request was not accepted
     */
    NOT_ACCEPTED,

    /**
     * Indicates that the student's application is under review
     */
    UNDER_APPRECIATION,

    /**
     * Indicates that the student's enrollment request was accepted
     */
    ACCEPTED;
}
